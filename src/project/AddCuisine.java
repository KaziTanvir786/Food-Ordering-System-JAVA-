    public void populateArrayList(){
        try{
            FileInputStream file = new FileInputStream("cuisine.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            
            boolean endOfFile = false;
            while(!endOfFile){
                try{
                    cuisines.add((Cuisine) inputFile.readObject());
                }catch(EOFException e){
                    endOfFile = true;
                }catch(Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile.close();
        }catch(IOException e){
            
        }
    }

    public void saveToFile(){
        try{
            FileOutputStream file = new FileOutputStream("Cuisine.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(int i=0; i<cuisines.size();i++){
                outputFile.writeObject(cuisines.get(i));
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null, "Cuisine has been added successfully");
            dispose();
            new AdminWindow().setVisible(true);
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
