    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(nameInp.getText().isEmpty()||priceInp.getText().isEmpty()||discountInp.getText().isEmpty()||descriptionInp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter the required fields!!");
        }
        else{
            String name = nameInp.getText().trim();
            String type = typeInp.getSelectedItem().toString();
            double price = Double.parseDouble(priceInp.getText());
            double discount = Double.parseDouble(discountInp.getText());
            String description = descriptionInp.getText();

            Cuisine cuisine = new Cuisine(name,type,price,discount,description);
            cuisines.add(cuisine);
            saveToFile();
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

