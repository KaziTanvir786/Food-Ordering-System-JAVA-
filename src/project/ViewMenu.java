    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextArea1.getText().isEmpty()||jTextArea2.getText().isEmpty()||jTextArea1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please select an item first and see the details!");
        }
        else if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter your name!");
        }
        else{
            int selectedIndex = jList1.getSelectedIndex();
            int quantity = Integer.parseInt((String)jComboBox1.getSelectedItem());
            jTextArea1.setText(cuisines.get(selectedIndex).toString());
            jTextArea2.setText(cuisines.get(selectedIndex).getDescription());
            Bill bill = new Bill (jTextField1.getText(),(cuisines.get(selectedIndex).getName()),(cuisines.get(selectedIndex).getPrice()),(cuisines.get(selectedIndex).getDiscount()),quantity);
            
            File f = new File("Bill/bill.txt");
            try {
                f.createNewFile();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            PrintWriter pw = null;
            try {
                pw = new PrintWriter(f);
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            String s = "Customer:\t"+bill.getCustomerName()+"\n"+bill.toString()+"\n\nThanks for ordering.";
            pw.print(s);
            pw.close();
            JOptionPane.showMessageDialog(null, "Thanks for your order");
            dispose();
            new MainMenu().setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

